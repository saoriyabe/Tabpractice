package practice.saori.tabpractice.data;

public class Quote {
    String quote;
    String author;

    public void setQuote(String quote) {
        this.quote = quote;
    }

    public void setAuthor(String author) {
        this.author = author;
    }

    public String getQuote() {
        return this.quote;
    }

    public String getAuthor() {
        return this.author;
    }
}
